Regex - Character Classes

Useful inbuilt character classes are listed below.  There are other inbuilt classes available for use, they are not limited to the list below.

[:alnum:] - checks for A-Za-z0-9

[:alpha:] - checks for A-Za-z

[:digit:] - checks for 0-9

[:space:] - checks for whitespace characters

[:lower:] - checks for lowercase letters a-z

[:upper:] - checks for uppercase letters A-Z

[:punct:] - checks for punctuation characters (eg # @ & $ ? etc)